/* ****************************************************************************************
 * Extern
 */
extern void lowlevel(void);
extern void setup(sys::Thread*);
extern void loop(sys::Thread*);

/* ****************************************************************************************
 * Class
 */

/* ****************************************************************************************
 * Variable
 */
static boot::CoreEntry* systemCoreEntry;

/* ****************************************************************************************
 * Method
 */

extern "C" int main(void){
  lowlevel();
  systemCoreEntry = new boot::CoreEntry(1024);
  systemCoreEntry->setSetup(setup);
  systemCoreEntry->setLoop(loop);
  sys::System::start(*systemCoreEntry);
}
 
